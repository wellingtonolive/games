import random


def play():
    imprime_mensagem_abertura()
    palavra_secreta = get_palavra_secreta()
    letras_acertadas = initialize_letras_acertadas(palavra_secreta)
    print(letras_acertadas)

    enforcou = False
    acertou = False
    erros = 0

    while not enforcou and not acertou:
        chute = get_chute_user()

        if chute in palavra_secreta:
            marca_chute_correto(chute, palavra_secreta, letras_acertadas)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = erros == 7
        acertou = '_' not in letras_acertadas

        print(letras_acertadas)

    if acertou:
        imprime_mensagem_sucesso()
    else:
        imprime_mensagem_perdedor(palavra_secreta)


def imprime_mensagem_fechamento():
    print("*********************************")
    print("******** Fim do Jogo ************")
    print("*********************************")


def imprime_mensagem_abertura():
    print("*********************************")
    print("Bem vindo ao jogo da Forca!")
    print("*********************************")


def get_palavra_secreta():
    arquivo = open("palavra.txt", "r")
    palavras = []

    for linha in arquivo:
        linha = linha.strip()
        palavras.append(linha)

    arquivo.close()
    numero = random.randrange(0, len(palavras))

    return palavras[numero].upper()


def initialize_letras_acertadas(palavra):
    return ["_" for letra in palavra]


def get_chute_user():
    chute = input("Qual a letra? ")
    return chute.strip().upper()


def marca_chute_correto(chute, palavra_secreta, letras_acertadas):
    index = 0
    for letra in palavra_secreta:
        if chute == letra:
            letras_acertadas[index] = letra
        index += 1


